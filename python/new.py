#strings - a sequence of characters or text that is enclosed in either single quotes (' ') or double quotes (" ")
message="Good morning, welcome to the world of programming!"
student_name="Alice"
print(message)
#type() confirms the data type of a variable!
print(type(message))
print(type(student_name)) #output class str

#integers - whole numbers without a decimal point
my_number=29
print(my_number)
print(type(my_number)) #output class int

#float- numbers with a decimal point
tax=21.59
print(tax)
print(type(tax)) #output class float

#boolean - a data type that can only have two values: True or False
is_raining=True
print(is_raining)
print(type(is_raining)) #output class bool

#complex numbers - numbers with a real and imaginary part 1+5j denoted with J
complex_num=3 + 4j
x=3+5j
print(x)
print(type(x))
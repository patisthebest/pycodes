num=7
num2=5
#arithmetic operand
print(num2+num)
print(num2-num)
print(num2*num)
print(num/num)
#comparison operator
print(num == num2) #num is equal to (false)
print(num != num2) #num is not equal to (true)
print(num > num2) #num is greater than num2(true)
print(num < num2) #num is less than num2 (false)
print(num <= num2) #num is less than or equal to num 2 (False)
print(num >= num2) #num is greater than or equal to num2 (true)

